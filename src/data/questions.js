// 선택지 순서 랜덤 셔플 함수
export function shuffleOptions(question) {
  const shuffled = [...question.options]
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]]
  }
  return {
    ...question,
    options: shuffled
  }
}
